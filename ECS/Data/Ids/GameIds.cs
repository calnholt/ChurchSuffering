    public static bool TryParseCardId(string key, out CardId id) => TryParseByKey(key, ToKey, out id);
    public static bool TryParseMedalId(string key, out MedalId id) => TryParseByKey(key, ToKey, out id);
    public static bool TryParseEquipmentId(string key, out EquipmentId id) => TryParseByKey(key, ToKey, out id);
    public static bool TryParseEnemyId(string key, out EnemyId id) => TryParseByKey(key, ToKey, out id);
    public static bool TryParseEnemyAttackId(string key, out EnemyAttackId id) => TryParseByKey(key, ToKey, out id);

    private static bool TryParseByKey<TId>(string key, Func<TId, string> toKey, out TId id)
        where TId : struct, Enum
    {
        id = default;
        if (string.IsNullOrWhiteSpace(key)) return false;
        foreach (var candidate in Enum.GetValues<TId>())
        {
            if (string.Equals(toKey(candidate), key.Trim(), StringComparison.OrdinalIgnoreCase))
            {
                id = candidate;
                return true;
            }
        }
        return false;
    }
